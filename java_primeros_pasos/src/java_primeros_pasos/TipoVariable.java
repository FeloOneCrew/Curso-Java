package java_primeros_pasos;

public abstract class TipoVariable {
	public static void main(String[] Args) {
		System.out.println("Hola Mundo");

		int edad = 33;
		System.out.println("La edad es: " + edad);

		String palabra = "Hola Mundo";
		System.out.println(palabra + ", La edad es: " + edad);

		double valor = 355.4455;
		System.out.println("El valor de la hora es: " + valor);

		int dias = 27;
		double GenerarSalario = valor * dias;
		System.out.println("El valor del salario es: " + GenerarSalario);

	}
}
